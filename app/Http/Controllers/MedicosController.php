    {
        $coMedicos = DB::table("medicos")
                         ->select(['id', 'nome', 'crm', 'especialidade', 'uf', 'created_at', 'updated_at'])
                         ->get();
        
        
        return DataTables::of($coMedicos)->addColumn('action', function ($coMedicos) {
            return '<a href="#top">Alterar</a> <a href="#top">Excluir</a>';
        })->make(true);
